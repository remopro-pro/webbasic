num1 = 22
num2 = 10

# 足し算
plus_result = num1 + num2
print(plus_result)

# 引き算
minus_result = num1 - num2
print(minus_result)

# 掛け算
multi_result = num1 * num2
print(multi_result)

# 割り算
divide_result = num1 / num2
print(divide_result)

# 消費税の計算
tax = 0.08
price = 1200

tax_price = price * (1 + tax)
print(tax_price)